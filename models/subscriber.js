import mongoose from "mongoose";

const subsscriberSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  subscribedToChannel: {
    type: String,
    required: true,
  },
  subscribeDate: {
    type: Date,
    required: true,
    default: Date.now,
  },
});

export const Subscriber = mongoose.model("Subscriber", subsscriberSchema);
